# Эта программа демонстрирует столбец из
# чисел с плавающей точкой,
# выровненных по десятичной точке.
num1 = 127.899
num2 = 3465.148
num3 = 3.776
num4 = 264.821
num5 = 88.081
num6 = 799.999

# Показать каждое число в поле из 7 символов
# с 2 десятичными знаками.
print(format(num1, '7.2f'))
print(format(num2, '7.2f'))
print(format(num3, '7.2f'))
print(format(num4, '7.2f'))
print(format(num5, '7.2f'))
print(format(num6, '7.2f'))
